month = int(input("please choose a month between 1 and 12?"))
if month >= 3 and month <= 5 :
    print("Spring runs")
elif month >= 6 and month <= 8 :
    print("Summer runs")
elif month >= 9 and month <= 11 :
    print("Autumn runs")
else :
    print("Winter runs")